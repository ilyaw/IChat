import UIKit

extension UIFont {
    
    static func avenir20() -> UIFont? {
        return UIFont(name: "avenir", size: 20)
    }
    
    static func avenir26() -> UIFont? {
        return UIFont(name: "avenir", size: 26)
    }
}
